#include "menu.h"

bool	ip_filter(char c)
{
	return (c == '.' || (c >= '0' && c <= '9'));
}

bool	name_filter(char c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9'));
}
