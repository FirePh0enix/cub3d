#include "vec.h"

t_v3	triangle_normal(t_v3 v0, t_v3 v1, t_v3 v2)
{
	t_v3	a;
	t_v3	b;
	t_v3	c;

	a = v3_sub(v1, v0);
	b = v3_sub(v2, v0);
	c = v3_norm(v3_cross(a, b));
	return (c);
}
